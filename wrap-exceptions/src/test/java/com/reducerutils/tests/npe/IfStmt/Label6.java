import java.io.File;

public class IfStmt6 {

    public boolean mut(Object foo, File bar) {
        if (bar == null)
            return true;
        if (bar == null)
            return true;
        if (bar.separator == "." && bar.pathSeparator == "/") {
            if (foo == null)
                return true;
            foo.toString();
        } else {
            if (foo == null)
                return true;
            foo.hashCode();
        }
        return false;
    }
}
